
public class Main_string_convertendo_para_minusculas {

	public static void main(String[] args) {
		
		String original = "abcde FGHIJ ABC abc DEFG		";
		
		String original_minusculas = original.toLowerCase();
		
		System.out.println("ORIGINAL: -" + original + "-");
		System.out.println(
				"FUNCAO '.toLowerCase();': -" + original_minusculas + "-"
				);

	}

}
